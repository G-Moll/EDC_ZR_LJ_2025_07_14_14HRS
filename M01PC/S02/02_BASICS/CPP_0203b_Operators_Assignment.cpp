#include <iostream>
using namespace std;

int main() {
    // Operands and Operators create Expressions, that calculate a new value
    // Sentence: A complete instruction
    int a = 20;

    // int a;
    // a = 20;

    int z = 10;
    cout << ( z ) << endl;

    z += 5;
    // z = z + 5;
    cout << ( z ) << endl;
    
    z -= 7;
    // z = z - 7;
    cout << ( z ) << endl;
    
    z *= 2;
    // z = z * 2;
    cout << ( z ) << endl;
    
    z %= 5;
    // z = z % 5;
    cout << ( z ) << endl;
    return 0;
}
