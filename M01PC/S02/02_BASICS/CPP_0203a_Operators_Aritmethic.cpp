#include <iostream>
using namespace std;

int main() {
    // Operands and Operators create Expressions, that calculate a new value

    int x = 10;
    int y = 4;
    cout << ( x + y ) << endl;
    cout << ( x - y ) << endl;
    cout << ( x / y ) << endl;
    cout << ( x * y ) << endl;
    cout << ( x % y ) << endl;
    return 0;
}
