#include <iostream>
using namespace std;

int main() {
    // > >= < >= == != (true 1/false 0)
    int a = 10;
    int z = 8;
    cout << ( a == z ) << endl;
    cout << ( a > z ) << endl;
    cout << ( a >= z ) << endl;
    cout << ( a < z ) << endl;
    cout << ( a <= z ) << endl;
    cout << ( a != z ) << endl;
    return 0;
}
