# 0202 Operadores Aritméticos

num_one = float( input( "Captura el primer número: " ) )
num_two = float( input( "Captura el segundo número: " ) )

print( "El primer número es: ", num_one )
print( "El segundo número es: ", num_two )
print( "La suma es: ", num_one + num_two )
print( "La resta es: ", num_one - num_two )
print( "La división es: ", num_one / num_two )
print( "La multiplicación es: ", num_one * num_two )
print( "El módulo es: ", num_one % num_two )




