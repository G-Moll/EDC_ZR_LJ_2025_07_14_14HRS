# Primitives Data Types
print( 10 )
print( 10.01 )
print( "Hello" )
print( 'aa' )
print( """aa""" )
print( '''aa''' )
print( True )
print( False )

# Complex Data Types
# Lists
#           0          1
print( [ "Joshua", "Peter" ] )

# Dictionaries
#           key: value (property)
print( { "name": "Joshua", "age": 30, "single": True } )

# Tuples
#           0          1
print( ( "Joshua", "Peter" ) )

